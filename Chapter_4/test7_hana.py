def to_start_pos(x):
    if x == 1:
        return 0
    elif x == 2:
        return 1
    elif x == 3:
        return 2
    elif x == 4:
        return 3
    elif x == -1:
        return -16
    elif x == -2:
        return -17
    elif x == -3:
        return -18
    elif x == -4:
        return -19